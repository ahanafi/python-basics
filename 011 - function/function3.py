# Fungsi dengan  input argumen / parameter

def jumlahkan(a, b):
    c = a + b
    print(c)

def kalikan(a, b):
    c = a * b
    print(c)

def bagikan(a, b):
    c = a / b
    print(c)

def kurangkan(a, b):
    c = a - b
    print(c)


jumlahkan(90, 30)
kalikan(30, 2)
bagikan(8, 2)
kurangkan(10, 5)
